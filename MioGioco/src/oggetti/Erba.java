package oggetti;

public class Erba
{
    private int x, y, tipo;

    public Erba (int x, int y, int tipo)
    {
        this.x = x;
        this.y = y;
        this.tipo = tipo;
    }

    public int getX()
    {
        return x;
    }

    public int getY()
    {
        return y;

    }

    public int getTipo()
    {
        return tipo;
    }
}
